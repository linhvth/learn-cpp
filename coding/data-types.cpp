#include <iostream>
using namespace std;

int main()
{
    bool testBoolean = false;          // boolean
    int testNum = 35;                  // Integer (whole number)
    float testFloat = 3.14;            // Floating point number
    double testDouble = 3.14159265359; // Floating point number
    char testChar = '3';

    cout << "Boolean value: " << testBoolean << endl
         << "Integer value: " << testNum << endl
         << "Float value: " << testFloat << endl
         << "Double value: " << testDouble << endl
         << "Char value: " << testChar << endl;
    return 0;
}
